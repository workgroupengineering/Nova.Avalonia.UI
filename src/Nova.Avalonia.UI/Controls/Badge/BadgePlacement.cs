namespace Nova.Avalonia.UI.Controls;

/// <summary>
/// Specifies the placement of the badge relative to its wrapped content.
/// </summary>
public enum BadgePlacement
{
    /// <summary>Badge appears at the top-left corner.</summary>
    TopLeft,
    /// <summary>Badge appears at the top center.</summary>
    Top,
    /// <summary>Badge appears at the top-right corner (default).</summary>
    TopRight,
    /// <summary>Badge appears at the center-right edge.</summary>
    Right,
    /// <summary>Badge appears at the bottom-right corner.</summary>
    BottomRight,
    /// <summary>Badge appears at the bottom center.</summary>
    Bottom,
    /// <summary>Badge appears at the bottom-left corner.</summary>
    BottomLeft,
    /// <summary>Badge appears at the center-left edge.</summary>
    Left
}