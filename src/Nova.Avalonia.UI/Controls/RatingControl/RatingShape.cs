namespace Nova.Avalonia.UI.Controls
{
    /// <summary>
    /// Specifies the shape of the items in the RatingControl.
    /// </summary>
    public enum RatingShape
    {
        /// <summary>
        /// Displays stars.
        /// </summary>
        Star,

        /// <summary>
        /// Displays hearts.
        /// </summary>
        Heart,

        /// <summary>
        /// Displays circles.
        /// </summary>
        Circle,

        /// <summary>
        /// Displays diamonds.
        /// </summary>
        Diamond,

        /// <summary>
        /// Displays a custom geometry defined by the CustomGeometry property.
        /// </summary>
        Custom
    }
}
