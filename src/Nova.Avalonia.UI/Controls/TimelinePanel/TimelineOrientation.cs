/// Specifies the orientation of a <see cref="TimelinePanel"/>.
/// </summary>
public enum TimelineOrientation
{
    /// <summary>
    /// Timeline flows horizontally from left to right.
    /// </summary>
    Horizontal,

    /// <summary>
    /// Timeline flows vertically from top to bottom.
    /// </summary>
    Vertical
}
