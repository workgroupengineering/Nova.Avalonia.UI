namespace Nova.Avalonia.UI.Controls;

/// <summary>
/// Specifies the direction to arrange children in a <see cref="CircularPanel"/>.
/// </summary>
public enum CircularOrientation
{
    /// <summary>
    /// Arrange children clockwise (default).
    /// </summary>
    Clockwise,

    /// <summary>
    /// Arrange children counter-clockwise.
    /// </summary>
    CounterClockwise
}
